import unittest
import math
import asteroids, ship, rock, star, bullet

class TestAsteroidsConstructorAndGetters( unittest.TestCase ):

    def setUp( self ):
        self.expected_rock_count = 10
        self.expected_star_count = 20
        self.expected_bullet_count = 0
        self.expected_object_count = 31
        self.expected_world_width = 800
        self.expected_world_height = 600
        self.constructed_obj = asteroids.Asteroids( self.expected_world_width, self.expected_world_height )
        
        return

    def tearDown( self ):
        return
    
    def test001_ConstructorSetsWorldWidth( self ):
        self.assertEqual( self.constructed_obj.getWorldWidth( ), self.expected_world_width )
        return
    
    def test002_ConstructorSetsWorldHeight( self ):
        self.assertEqual( self.constructed_obj.getWorldHeight( ), self.expected_world_height )
        return

    def test003_ConstructorCreatesShip( self ):
        ship_obj = self.constructed_obj.getShip( )
        self.assertEqual( isinstance( ship_obj, ship.Ship ), True )
        return

    def test004_ConstructorCreatesRocks( self ):
        rocks = self.constructed_obj.getRocks( )
        self.assertEqual( len( rocks ), self.expected_rock_count )
        for rock_obj in rocks:
            self.assertEqual( isinstance( rock_obj, rock.Rock ), True )
        return

    def test005_ConstructorCreatesObjects( self ):
        objects = self.constructed_obj.getObjects( )
        self.assertEqual( len( objects ), self.expected_object_count )
        return
    
    def test006_ConstructorCreatesStars( self ):
        stars = self.constructed_obj.getStars( )
        self.assertEqual( len( stars ), self.expected_star_count )
        for star_obj in stars:
            self.assertEqual( isinstance( star_obj, star.Star ), True )
        return

    def test007_ConstructorCreatesBullets( self ):
        bullets = self.constructed_obj.getBullets( )
        self.assertEqual( len( bullets ), self.expected_bullet_count )
        return


   
def suite( ):
    return unittest.TestLoader( ).loadTestsFromTestCase( TestAsteroidsConstructorAndGetters )

if __name__ == '__main__':
    runner = unittest.TextTestRunner( )
    runner.run( suite( ) )
